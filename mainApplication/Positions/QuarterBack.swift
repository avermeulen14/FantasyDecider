import Foundation

//class that will define the QuarterBack position. 
class QuarterBack
{
  private var passingAttempts: Float
  {
    get { return passingAttempts}
    set { passingAttempts = newValue}
  }

  private var completions: Float
  {
    get { return completions}
    set { completions = newValue}
  }

  private var totalPassingYards: Float
  {
    get { return totalPassingYards}
    set { totalPassingYards = newValue}
  }

  private var touchdownsThrown: Float
  {
    get { return touchdownsThrown}
    set { touchdownsThrown = newValue}
  }

  private var interceptions: Float
  {
    get { return interceptions}
    set { interceptions = newValue}
  }

  private var rushingAttempts: Float
  {
    get { return rushingAttempts}
    set { rushingAttempts = newValue}
  }

  private var rushingYards: Float
  {
    get { return rushingYards}
    set { rushingYards = newValue}
  }

  private var rushingTouchdowns: Float
  {
    get { return rushingTouchdowns}
    set { rushingTouchdowns = newValue}
  }

  private var age: Int
  {
    get { return age}
    set { age = newValue}
  }

  private var name: String
  {
    get { return name}
    set { name = newValue}
  }


}
